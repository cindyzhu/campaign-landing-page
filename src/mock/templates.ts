import { nanoid } from 'nanoid';
import type { ComponentNode, PageConfig } from '@/types/page';

export interface PageTemplate {
  id: string;
  name: string;
  description: string;
  thumbnail: string;
  category: 'flash-sale' | 'new-arrival' | 'holiday' | 'brand' | 'year-end' | 'mothers-day' | 'community' | 'summer' | 'spring' | 'golden-week';
  config: PageConfig;
  components: ComponentNode[];
}

function createComponent(
  type: ComponentNode['type'],
  props: Record<string, unknown>,
  style: ComponentNode['style'] = {},
  overrides: Partial<ComponentNode> = {}
): ComponentNode {
  return {
    id: nanoid(),
    type,
    props,
    style,
    loadPriority: 'critical',
    visible: true,
    locked: false,
    ...overrides,
  };
}

